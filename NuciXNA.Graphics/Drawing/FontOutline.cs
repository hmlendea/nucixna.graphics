namespace NuciXNA.Graphics.Drawing
{
    /// <summary>
    /// Specifies the outline style applied to drawn text.
    /// </summary>
    public enum FontOutline
    {
        /// <summary>No outline is applied.</summary>
        None,

        /// <summary>An outline is drawn on all four sides of the text.</summary>
        Around,

        /// <summary>An outline is drawn only at the bottom-right of the text.</summary>
        BottomRight
    }
}
